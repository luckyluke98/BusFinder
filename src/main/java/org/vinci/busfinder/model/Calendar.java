package org.vinci.busfinder.model;

import jakarta.persistence.*;

import java.time.LocalDate;

@Entity
@Table(name = "calendar")
public class Calendar {

    @Id
    @Column(name = "service_id")
    private String serviceId;

    @Column(name = "monday")
    private boolean monday;

    @Column(name = "tuesday")
    private boolean tuesday;

    @Column(name = "wednesday")
    private boolean wednesday;

    @Column(name = "thursday")
    private boolean thursday;

    @Column(name = "friday")
    private boolean friday;

    @Column(name = "saturday")
    private boolean saturday;

    @Column(name = "sunday")
    private boolean sunday;

    @Column(name = "start_date", columnDefinition = "DATETIME")
    private LocalDate startDate;

    @Column(name = "end_date", columnDefinition = "DATETIME")
    private LocalDate endDate;

    public Calendar() {}

    @Override
    public String toString() {
        return String.format(
                "Calendar[service_id=%s, monday=%s, tuesday=%s, wednesday=%s " +
                        "thursday=%s, friday=%s, saturday=%s, sunday=%s, startDate=%s, endDate=%s]",
                serviceId, monday, tuesday, wednesday, thursday, friday, saturday, sunday,
                startDate.toString(), endDate.toString()
        );
    }

    public String getServiceId() {
        return serviceId;
    }

    public void setServiceId(String serviceId) {
        this.serviceId = serviceId;
    }

    public boolean isMonday() {
        return monday;
    }

    public void setMonday(boolean monday) {
        this.monday = monday;
    }

    public boolean isTuesday() {
        return tuesday;
    }

    public void setTuesday(boolean tuesday) {
        this.tuesday = tuesday;
    }

    public boolean isWednesday() {
        return wednesday;
    }

    public void setWednesday(boolean wednesday) {
        this.wednesday = wednesday;
    }

    public boolean isThursday() {
        return thursday;
    }

    public void setThursday(boolean thursday) {
        this.thursday = thursday;
    }

    public boolean isFriday() {
        return friday;
    }

    public void setFriday(boolean friday) {
        this.friday = friday;
    }

    public boolean isSaturday() {
        return saturday;
    }

    public void setSaturday(boolean saturday) {
        this.saturday = saturday;
    }

    public boolean isSunday() {
        return sunday;
    }

    public void setSunday(boolean sunday) {
        this.sunday = sunday;
    }

    public LocalDate getStartDate() {
        return startDate;
    }

    public void setStartDate(LocalDate startDate) {
        this.startDate = startDate;
    }

    public LocalDate getEndDate() {
        return endDate;
    }

    public void setEndDate(LocalDate endDate) {
        this.endDate = endDate;
    }
}
